                 <h2>7. <u>STOOL OCCULT BLOOD</u></h2>
                 <p>A stool occult blood test, also known as faecal occult blood test (FOBT), is a medical test used to detect the presence of small, invisible amounts of blood in the stool. The term "occult" means hidden or concealed, so this test is designed to identify blood that is not visible to the naked eye. The presence of occult blood in the stool can be an early indicator of various gastrointestinal problems, including bleeding in the digestive tract.</p>
                 <p>Here are some key points about stool occult blood tests:-</p>
                 <h6>1. Purpose:</h6>
                 <p>The primary purpose of a stool occult blood test is to screen for gastrointestinal bleeding. Gastrointestinal bleeding can be a sign of various conditions, including colorectal cancer, polyps, ulcerative colitis, Crohn's disease, haemorrhoids, and other gastrointestinal disorders.</p>
                 <h6>2. Screening and Diagnostic Use:</h6>
                 <p>Stool occult blood tests are commonly used as a non-invasive screening tool to detect early signs of colorectal cancer. They are also used diagnostically to investigate the cause of gastrointestinal symptoms like unexplained anaemia, abdominal pain, or changes in bowel habits.</p>
                 <h6>3. Sample Collection:</h6>
                 <p>For both types of FOBTs, a small stool sample is collected by the patient. The sample is typically collected at home using a special collection kit provided by a healthcare provider. The sample is then returned to the healthcare facility for analysis.</p>
                 <h6>4. Dietary Restrictions: </h6>
                 <p>In the past, gFOBT required dietary restrictions, as certain foods (like red meat and some medications) could produce false-positive results. FIT, on the other hand, does not usually require dietary restrictions.</p>
                 <h6>5.Interpretation of Results:</h6>
                 <p> If the test is positive for occult blood, further diagnostic tests, such as a colonoscopy or sigmoidoscopy, may be recommended to investigate the source of bleeding and to rule out serious conditions like colorectal cancer.</p>
                 <h6>6. Frequency of Testing:</h6>
                 <p>The recommended frequency of stool occult blood testing varies based on individual risk factors and guidelines from healthcare organisations. Screening is often recommended for individuals over a certain age (typically starting at 50) and may be done annually or at intervals specified by a healthcare provider.</p>
                 <span>It's important to note that a positive result on a stool occult blood test does not necessarily mean a person has cancer or a severe condition. It indicates the need for further investigation to determine the cause of the bleeding.</span>
